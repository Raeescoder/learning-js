// Declare a variable x and assign it the string value "hello world"
var x = 'hello world';
// Write the value of x to the document
document.write(x);
// Write the data type of x to the document
document.write(typeof x); // outputs "string"

// Declare a variable z and assign it the numeric value 32
var z = 32;
// Write the value of z to the document
document.write(z);
// Write a line break to the document
document.write('<br>');
// Write the data type of z to the document
document.write(typeof z); // outputs "number"

// Declare a variable s and assign it the value null
var s = null;
// Write the data type of s to the document
document.write(typeof s); // outputs "object" because null is considered an object type
// Write a line break to the document
document.write("<br>");

// Declare an object myObject with three properties: name, age, and occupation
let myObject = { name: 'John', age: 30, occupation: 'Developer' };
// Write the object to the document
document.write(myObject); // outputs [object Object]
// Write the data type of myObject to the document
document.write(typeof myObject); // outputs "object"

// Declare an array myArray with three elements: a string, a number, and another string
let myArray = ['ali', 32, 'gaon'];
// Write the array to the document
document.write(myArray); // outputs "ali,32,gaon"
// Write the data type of myArray to the document
document.write(typeof myArray); // outputs "object" because arrays are considered objects

