// Arithmetic Operations in JavaScript

// Addition
let a = 5;
let b = 3;
let result = a + b;
console.log("Addition: " + a + " + " + b + " = " + result);

// Subtraction
a = 5;
b = 3;
result = a - b;
console.log("Subtraction: " + a + " - " + b + " = " + result);

// Multiplication
a = 5;
b = 3;
result = a * b;
console.log("Multiplication: " + a + " * " + b + " = " + result);

// Division
a = 10;
b = 2;
result = a / b;
console.log("Division: " + a + " / " + b + " = " + result);

// Modulus
a = 10;
b = 3;
result = a % b;
console.log("Modulus: " + a + " % " + b + " = " + result);

// Exponentiation
a = 2;
b = 3;
result = a ** b;
console.log("Exponentiation: " + a + " ** " + b + " = " + result);

// Using assignment operators
a = 5;
a += 3; // equivalent to a = a + 3
console.log("Assignment Operator: a += 3, a = " + a);

a = 5;
a -= 3; // equivalent to a = a - 3
console.log("Assignment Operator: a -= 3, a = " + a);

a = 5;
a *= 3; // equivalent to a = a * 3
console.log("Assignment Operator: a *= 3, a = " + a);

a = 10;
a /= 2; // equivalent to a = a / 2
console.log("Assignment Operator: a /= 2, a = " + a);

a = 10;
a %= 3; // equivalent to a = a % 3
console.log("Assignment Operator: a %= 3, a = " + a);

a = 2;
a **= 3; // equivalent to a = a ** 3
console.log("Assignment Operator: a **= 3, a = " + a);